package com.TugasPertemuan4;

public class Matematika {
    int Tambah(int a, int b){
        int HasilTambah = a + b;
        return HasilTambah;
    }

     int Kurang(int a,int b){
        int HasilKurang = a - b;
        return HasilKurang;
    }

     int Perkalian(int a, int b){
        int HasilKali = a * b;
        return HasilKali;
    }

     int Pembagian(int a, int b){
        int HasilBagi = a/b;
        return HasilBagi;
    }
}
